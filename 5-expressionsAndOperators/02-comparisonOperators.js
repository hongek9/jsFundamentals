// Comparison Operators

// equal

'3' == 3; // true

// strictly equals

3 === 3; // true
'3' === 3; // false

// does not equal

4 != '3';  // true

// strictly not equal 

'3' !== 3; //true


// greater than

3 > 2; 

// less than

2 < 3; 

// greater than or equal to

3 >= 2;

// less than of equal to

2 <= 3;

// and

2 && 3;

// or

2 || 3;
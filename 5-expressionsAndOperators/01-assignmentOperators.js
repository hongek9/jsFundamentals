// Assignment Operators

// Assignment Operator - Equals sign
let x = 5;

// Addition Assignment - plus sign
x = x + 1;
x += 1;
x++;

// Subtraction Assignment - minus sign
x = x - 1;
x -= 1;
x--;

// Multiplication Assignment - asterick
x = x * 1
x *= 1;

// Division Assignment - forward slash
x = x / 1;
x /= 1;

// Remainder/ Modulus Assignment
x = x % 1;
x%= 1;

// Exponential Assignment
x = x**1;
x**=1;
